export default function fullName(name) {
    switch (name) {
        case 'ACS':
            return 'AC Statville'
            break
        case 'ZRT':
            return 'Zenith Royale Tremt'
            break
        case 'CMT':
            return 'Madison Acres Comets'
            break
        case 'FTN':
            return 'Fortuna Madison'
            break
        case 'CNT':
            return 'Central Statville'
            break
        case 'ARD':
            return 'Arlington Dragons'
            break
        case 'ARS':
            return 'Arlington Stingers'
            break
        case 'EFC':
            return 'East Statville'
            break
        case 'OLS':
            return 'Olympique Statville'
            break
        case 'KMN':
            return 'Atletico Kamino Stat.'
            break
        default:
            return name
            break
    }
}